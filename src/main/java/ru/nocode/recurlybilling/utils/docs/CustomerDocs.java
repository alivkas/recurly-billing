    public static final String GET_SUMMARY = "🔎 Получить данные клиента";

    public static final String GET_DESCRIPTION = """
            Возвращает полную информацию о клиенте по его внешнему идентификатору.
            
            **Используйте для:**
            - Отображения профиля плательщика в личном кабинете
            - Проверки контактных данных перед отправкой чека
            - Привязки истории платежей к пользователю в вашей системе
            
            **Поиск по `externalId`:**
            
            Система ищет клиента по комбинации:
            ```
            WHERE tenant_id = ? AND external_id = ?
            ```
            
            Это гарантирует, что вы не получите данные клиента из другой организации.
            """;

    public static final String LIST_SUMMARY = "📋 Получить всех клиентов тенанта";

    public static final String LIST_DESCRIPTION = """
            Возвращает список всех клиентов, зарегистрированных в организации.
            
            **Когда использовать:**
            - Экспорт базы плательщиков для аналитики
            - Массовая рассылка уведомлений об изменениях тарифов
            - Синхронизация данных с внешней CRM
            
            **Пагинация (планируется):**
            В будущих версиях поддерживаются параметры:
            - `?limit=50` — количество записей на странице (по умолчанию 20)
            - `?offset=0` — смещение от начала списка
            - `?search=ivan` — фильтрация по имени, фамилии или email
            
            **Производительность:**
            
            Для тенантов с большим количеством клиентов (>10 000) рекомендуется:
            - Использовать пагинацию
            - Кэшировать результаты на стороне клиента
            - Запрашивать только необходимые поля (через `?fields=id,externalId,email`)
            """;

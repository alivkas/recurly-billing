    public static final String LOGIN_DESCRIPTION = """
            Аутентифицирует студента и выдаёт токен доступа.
            
            **Параметры запроса:**
            
            | Поле | Тип | Описание | Пример |
            |------|-----|----------|--------|
            | `tenantId` | String | ID организации (из вашей системы) | `6546tds` |
            | `studentExternalId` | String | Внешний ID студента (из LMS) | `student_ivan_2024` |
            | `code` | String | Одноразовый код подтверждения | `847291` |
            
            **Как работает проверка:**
            
            1. Система ищет активный код для комбинации `tenantId + studentExternalId`
            2. Сравнивает переданный код с хешем в базе (не в открытом виде!)
            3. Проверяет, не истёк ли срок действия кода (обычно 5-10 минут)
            4. При успехе генерирует JWT-токен с claims: `sub`, `tenant`, `exp`
            5. Возвращает токен клиенту для использования в `Authorization: Bearer ...`
            
            **Формат токена (JWT):**
            ```json
            {
              "sub": "student_ivan_2024",
              "tenant": "6546tds",
              "iat": 1711922858,
              "exp": 1712009258,
              "scope": "student:read student:content"
            }
            ```
            
            **Использование токена:**
            ```http
            GET /api/v1/access/check?studentId=student_ivan_2024&planCode=premium
            Authorization: Bearer eyJhbGciOiJIUzI1NiIsInR5cCI6IkpXVCJ9...
            ```
            
            **Обработка ошибок:**
            
            | Ошибка | Когда возникает | Что делать |
            |--------|----------------|------------|
            | `INVALID_CODE` | Код не совпадает с выданным | Предложить ввести код заново |
            | `CODE_EXPIRED` | Код истёк (>10 минут) | Отправить новый код |
            | `TOO_MANY_ATTEMPTS` | Превышен лимит попыток | Подождать 15 минут или запросить новый код |
            | `STUDENT_NOT_FOUND` | Студент не найден в тенанте | Проверить корректность studentExternalId |
            | `TENANT_NOT_FOUND` | Неверный tenantId | Проверить настройки интеграции |
            
            **Рекомендации для фронтенда:**
            
            - Показывайте таймер обратного отсчёта действия кода
            - Добавьте кнопку «Отправить код повторно» с задержкой 60 секунд
            - После успешного входа сохраняйте токен в `localStorage` или `HttpOnly cookie`
            - Реализуйте автоматическое обновление токена перед истечением (refresh token)
            """;


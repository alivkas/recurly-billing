    public static final String TAG_DESCRIPTION = """
            Аутентификация студентов по одноразовому коду.
            
            **Зачем нужен этот эндпоинт?**
            
            `StudentAuthController` реализует безопасный вход студента в систему 
            без использования пароля — через **одноразовый код подтверждения**.
            
            Типичный сценарий:
            ```
            1. Студент вводит email/телефон в форме входа
            2. Система отправляет код (6 цифр) через смс или письмо
            3. Студент вводит код → вызывает POST /api/v1/student/login
            4. При успехе получает JWT-токен для последующих запросов
            5. Токен используется в заголовке Authorization для доступа к контенту
            ```
            
            **Преимущества подхода:**
            
            - 🔐 **Без паролей**: нет риска утечки, не нужно хранить хеши
            - ⏱ **Временные коды**: код действует 5-10 минут, затем невалиден
            - 📱 **Удобство**: студенту не нужно запоминать пароль
            - 🛡 **Защита от брутфорса**: лимит попыток ввода кода (3-5 раз)
            
            **Безопасность:**
            
            - Коды хранятся в зашифрованном виде с TTL (автоматическое удаление)
            - После 3 неудачных попыток — временная блокировка на 15 минут
            - Токены имеют срок жизни (например, 24 часа) и могут быть отозваны
            - Все попытки входа логируются в аудит-лог для мониторинга
            
            **Важно:**
            
            - Этот эндпоинт **публичный** — не требует заголовков `X-Tenant-ID` / `X-API-Key`
            - `tenantId` передаётся в теле запроса и валидируется вместе с кодом
            - Ответ содержит только токен — никаких персональных данных студента
            """;


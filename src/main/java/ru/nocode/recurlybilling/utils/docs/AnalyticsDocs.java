    public static final String GET_SUMMARY = "📊 Получить бизнес-аналитику";

    public static final String GET_DESCRIPTION = """
            Возвращает сводные метрики бизнеса для организации за текущий период.
            
            **Период по умолчанию:**
            - Текущий календарный месяц (с 1-го числа по сегодня)
            - Сравнение с предыдущим периодом той же длины (для расчёта динамики)
            
            **Параметры запроса (планируется):**
            В будущих версиях поддерживаются:
            - `?period=month|week|day|custom` — гранулярность данных
            - `?startDate=2026-01-01&endDate=2026-03-31` — произвольный диапазон
            - `?groupBy=day|week|month` — группировка временных рядов
            - `?metrics=revenue,subscriptions,customers` — только нужные метрики
            
            **Структура ответа:**
            
            Ответ содержит четыре основных блока:
            
            1. **💰 Revenue** — метрики выручки:
               - `mrr` — Monthly Recurring Revenue (ежемесячная повторяющаяся выручка)
               - `totalRevenue` — общая сумма всех платежей за период
               - `averageCheck` — средний чек
               - `growth` — изменение относительно прошлого периода (%)
            
            2. **🔄 Subscriptions** — метрики подписок:
               - `active` — количество активных подписок на конец периода
               - `new` — создано за период
               - `canceled` — отменено за период
               - `churnRate` — коэффициент оттока (%)
            
            3. **👥 Customers** — метрики клиентов:
               - `total` — всего уникальных клиентов
               - `new` — новых клиентов за период
               - `activePayers` — клиентов с хотя бы одним успешным платежом
            
            4. **💳 Payments** — метрики платежей:
               - `successful` — успешно проведённых
               - `failed` — неудачных попыток
               - `refunded` — возвратов средств
               - `conversionRate` — конверсия успешных платежей (%)
            
            **Пример использования в дашборде:**
            
            ```javascript
            // Загрузка аналитики для графика
            fetch('/api/v1/analytics', {
              headers: {
                'X-Tenant-ID': tenantId,
                'X-API-Key': apiKey
              }
            })
            .then(r => r.json())
            .then(data => {
              // Обновляем карточки метрик
              document.getElementById('mrr').textContent = 
                formatCurrency(data.revenue.mrr);
              document.getElementById('mrr-growth').className = 
                data.revenue.growth >= 0 ? 'growth-positive' : 'growth-negative';
              
              // Рисуем график динамики
              renderChart(data.revenue.timeseries);
            });
            ```
            
            **Совет:**
            Для отображения аналитики в реальном времени настройте периодический 
            опрос этого эндпоинта (polling) с интервалом 5-15 минут.
            """;


    public static final String BY_RESOURCE_SUMMARY = "🔗 Получить логи по ресурсу";

    public static final String BY_RESOURCE_DESCRIPTION = """
            Возвращает историю изменений конкретного ресурса системы.
            
            **Формат пути ресурса:**
            ```
            /api/v1/audit/resource/{resourceType}/{resourceId}
            ```
            
            | resourceType | Пример resourceId | Описание |
            |--------------|------------------|----------|
            | `subscription` | `sub_abc123` | История подписки |
            | `payment` | `payment_456` | История платежа |
            | `customer` | `user_ivan_2024` | История клиента |
            | `plan` | `plan_premium` | История тарифа |
            | `tenant` | `6546tds` | История организации |
            
            **Когда использовать:**
            - «Почему статус этой подписки изменился на `canceled`?»
            - «Кто обновил настройки этого тарифного плана?»
            - «Показать полную историю жизни платежа»
            
            **Структура записи аудита:**
            Каждая запись содержит:
            - `timestamp` — точное время события
            - `eventType` — тип действия (`created`, `updated`, `deleted`, `accessed`)
            - `actor` — кто выполнил действие (API-ключ, пользователь, система)
            - `resource` — что было изменено (тип + ID)
            - `changes` — что именно изменилось (старое → новое значение)
            - `ipAddress` — IP-адрес источника запроса
            - `userAgent` — заголовок User-Agent клиента
            """;

    public static final String TAG_DESCRIPTION = """
            Управление подписками клиентов на рекуррентные платежи.
            
            **Что такое подписка?**
            
            Подписка — это договорённость о периодическом списании средств 
            с карты клиента в пользу организации (тенанта). Используется для:
            - Оплаты доступа к образовательным курсам
            - Ежемесячных членских взносов
            - Автоматического продления лицензий
            
            **Жизненный цикл подписки:**
            ```
            created → trialing → active → (renewed × N) → canceled/expired
            ```
            
            **Авторизация**
            
            Все запросы требуют заголовки:
            - `X-Tenant-ID` — идентификатор вашей организации
            - `X-API-Key` — секретный ключ (выдаётся при регистрации)
            """;

    public static final String CREATE_SUMMARY = "🔄 Создать новую подписку";

    public static final String CREATE_DESCRIPTION = """
            Создаёт подписку для клиента и инициирует первый платёж (если нет пробного периода).
            
            **Когда использовать:**
            - Клиент оформил покупку курса с ежемесячной оплатой
            - Пользователь активировал авто-продление подписки
            - Администратор вручную создал подписку для ученика
            
            **Особенности:**
            - Поддерживается **идемпотентность**: передайте `Idempotency-Key`, 
              чтобы избежать дублирования при повторных запросах
            - Если у подписки есть пробный период (`trial_days > 0`), 
              первый платёж не создаётся сразу — статус будет `trialing`
            - При успешном создании платежа возвращается ссылка на подтверждение 
              (для 3-D Secure или редиректа в платёжную систему)
            
            **Идемпотентность:**
            
            Заголовок `Idempotency-Key` гарантирует, что повторный запрос 
            с тем же ключом не создаст дубликат подписки или платежа.
            Если ключ не передан — он генерируется автоматически (но тогда 
            повторный запрос может создать дубликат).
            
            **Рекомендация:**
            Всегда генерируйте `Idempotency-Key` на стороне клиента 
            (например, UUID) и сохраняйте его до получения успешного ответа.
            """;

